/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------- What product line had the largest VAT?---------------------------------------------- */


SELECT
product_line,
AVG(Tax_5) as avg_tax
FROM [dbo].[Walmart_Sales]
GROUP BY product_line
ORDER BY avg_tax DESC;

/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------
--Fetch each product line and add a column to those product line showing "Good", "Bad". Good if it's greater than average sales?--------
-------------------------------------------------------------------------------------------------------------------------------------- */

WITH CTE_Sub_Table AS (
SELECT ROUND(AVG(Total),2) Avg_Total_Sales
FROM Walmart_Sales
)
SELECT Product_Line, ROUND(SUM(Total),2) AS Total_Sales,ROUND(AVG(Total),2) AS Avg_Sales, 
CASE
WHEN AVG(Total) > (SELECT * FROM CTE_Sub_Table) THEN 'Good'
ELSE 'Bad'
END AS Status_of_Sales
FROM Walmart_Sales
GROUP BY Product_Line
ORDER BY 2 DESC;

/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------- Which branch sold more products than average product sold?-------------------------------------------- */


SELECT 
Branch, SUM(quantity) AS Qty
FROM Walmart_Sales
GROUP BY branch
HAVING SUM(quantity) > (SELECT AVG(quantity) FROM Walmart_Sales);


/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------- What is the most common product line by gender?------------------------------------------- */


SELECT
gender, product_line,
COUNT(gender) AS total_cnt
FROM  Walmart_Sales
GROUP BY gender, product_line
ORDER BY total_cnt DESC;

/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------- What is the average rating of each product line?--------------------------------------- */



SELECT
ROUND(AVG(rating), 2) as avg_rating, product_line
FROM  Walmart_Sales
GROUP BY product_line
ORDER BY avg_rating DESC;


