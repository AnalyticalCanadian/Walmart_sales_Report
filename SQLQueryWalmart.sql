Use Walmart_Dataset_Kaggle

CREATE TABLE dbo.Walmart_Sales
(
    Invoice_ID NVARCHAR(50) NOT NULL PRIMARY KEY,
    Branch NVARCHAR(50) NOT NULL,
    City NVARCHAR(50) NOT NULL,
    Customer_type NVARCHAR(50) NOT NULL,
    Gender VARCHAR(30) NOT NULL,
    Product_line NVARCHAR(50) NOT NULL,
    Unit_price DECIMAL(18,10) NOT NULL,
    Quantity INT NOT NULL,
    tax_5 DECIMAL(18,10) NOT NULL,
    total DECIMAL(18, 10) NOT NULL,
    Date DATE NOT NULL,
    Time TIME NOT NULL,
    Payment NVARCHAR(50) NOT NULL,
    Cogs DECIMAL(18,10) NOT NULL,
    Gross_margin_percentage DECIMAL(18,10) NOT NULL,
    Gross_income DECIMAL(18, 10) NOT NULL,
    Rating DECIMAL(18,10) NOT NULL

    /* Select * from Walmart_sales;*/ 

SELECT Time,
    (CASE
        WHEN CAST(Time AS time) BETWEEN '00:00:00' AND '12:00:00' THEN 'Morning'
        WHEN CAST(Time AS time) BETWEEN '12:01:00' AND '16:00:00' THEN 'Afternoon'
        ELSE 'Evening'
     END) AS time_of_day
FROM [dbo].[Walmart_Sales]
;

ALTER TABLE Walmart_Sales ADD MonthName Varchar (10);
UPDATE Walmart_Sales SET MonthName = DATENAME(month, Date);
Select Walmart_Sales.MonthName from Walmart_Sales;


