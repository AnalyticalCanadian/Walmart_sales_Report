--------------------------------------------- How many unique product lines does the data have?------------------------------------ */




SELECT
DISTINCT product_line,
FROM [dbo].[Walmart_Sales];


/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------- What is the Profit & profit Margin of each product?-------------------------------------------- */


SELECT Product_line, sum(Total - cogs) AS profit,
sum((total - cogs) / total) * 100 AS profit_margin
FROM dbo.Walmart_Sales
group by Product_line
Order by profit,profit_margin;



/* -------------------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------------------
------------------------------------------------- What is the most selling product line?-------------------------------------------- */



SELECT
SUM(quantity) as qty, product_line, Walmart_Sales.MonthName
FROM [dbo].[Walmart_Sales]
GROUP BY product_line, Walmart_Sales.MonthName
ORDER BY qty DESC;

